def _generate_pdf_report_sync(
    title: str,
    content: str,
    output_path: str = "./reports/report.pdf",
    subtitle: str | None = None,
) -> str:
    """Generate a styled PDF report.

    Args:
        title: The title of the report
        content: The main content of the report
        output_path: The path where the PDF will be saved (default: ./reports/report.pdf)
        subtitle: Optional subtitle for the report

    Returns:
        A message indicating success or failure with the file path
    """
    try:
        prepare_report_output_path(output_path)

        # Create PDF document
        doc = SimpleDocTemplate(
            output_path,
            pagesize=letter,
            rightMargin=0.75 * inch,
            leftMargin=0.75 * inch,
            topMargin=0.75 * inch,
            bottomMargin=0.75 * inch,
        )

        # Get sample styles
        styles = getSampleStyleSheet()

        # Create custom styles
        title_style = ParagraphStyle(
            "CustomTitle",
            parent=styles["Heading1"],
            fontSize=24,
            textColor=colors.HexColor("#2c3e50"),
            spaceAfter=30,
            alignment=1,  # Center
        )

        subtitle_style = ParagraphStyle(
            "CustomSubtitle",
            parent=styles["Heading2"],
            fontSize=14,
            textColor=colors.HexColor("#34495e"),
            spaceAfter=20,
            alignment=1,  # Center
        )

        body_style = ParagraphStyle(
            "CustomBody",
            parent=styles["BodyText"],
            fontSize=11,
            leading=14,
            textColor=colors.HexColor("#2c3e50"),
            spaceAfter=12,
        )

        # Build content
        story = []

        # Add title
        story.append(Paragraph(title, title_style))

        # Add subtitle if provided
        if subtitle:
            story.append(Paragraph(subtitle, subtitle_style))

        # Add date
        date_style = ParagraphStyle(
            "DateStyle",
            parent=styles["Normal"],
            fontSize=10,
            textColor=colors.HexColor("#7f8c8d"),
            alignment=1,
        )
        story.append(
            Paragraph(
                f"Generated on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
                date_style,
            )
        )
        story.append(Spacer(1, 0.3 * inch))

        # Add horizontal line
        line_table = Table(
            [[""]],
            colWidths=[7.5 * inch],
        )
        line_table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (-1, -1), colors.HexColor("#ecf0f1")),
                    ("LINEBELOW", (0, 0), (-1, -1), 2, colors.HexColor("#3498db")),
                    ("ROWHEIGHTS", (0, 0), (-1, -1), 0.05 * inch),
                ]
            )
        )
        story.append(line_table)
        story.append(Spacer(1, 0.2 * inch))

        # Add main content (split by lines)
        content_lines = content.split("\n")
        for line in content_lines:
            if line.strip():
                story.append(Paragraph(line.strip(), body_style))
            else:
                story.append(Spacer(1, 0.1 * inch))

        # Add footer
        story.append(Spacer(1, 0.3 * inch))
        footer_style = ParagraphStyle(
            "FooterStyle",
            parent=styles["Normal"],
            fontSize=9,
            textColor=colors.HexColor("#95a5a6"),
            alignment=1,
        )
        story.append(
            Paragraph("Generated by WhatsApp Messaging Agent", footer_style)
        )

        # Build PDF
        doc.build(story)

        file_size = os.path.getsize(output_path)
        return f"PDF report successfully generated: {output_path} ({file_size} bytes)"

    except Exception as e:
        return f"Error generating PDF report: {str(e)}"


