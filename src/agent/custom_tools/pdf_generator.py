def _generate_messaging_report_pdf_sync(
    scope_name: str,
    analysis_date: str,
    stats: dict,
    top_opportunities: list[dict],
    generated_replies: list[dict],
    recommendations: list[str],
    output_path: str = "./reports/whatsapp_messaging_report.pdf",
    chat_metadata: dict | None = None,
    failed_replies: list[dict] | None = None,
) -> str:
    """Generate a WhatsApp messaging PDF report with tables."""
    try:
        prepare_report_output_path(output_path)

        theme = WHATSAPP_THEME
        doc = SimpleDocTemplate(
            output_path,
            pagesize=letter,
            rightMargin=0.65 * inch,
            leftMargin=0.65 * inch,
            topMargin=0.65 * inch,
            bottomMargin=0.65 * inch,
        )
        styles = getSampleStyleSheet()

        title_style = ParagraphStyle(
            "WATitle",
            parent=styles["Heading1"],
            fontSize=22,
            textColor=colors.HexColor(theme["title"]),
            spaceAfter=8,
            alignment=1,
            fontName="Helvetica-Bold",
        )
        section_style = ParagraphStyle(
            "WASection",
            parent=styles["Heading2"],
            fontSize=13,
            textColor=colors.HexColor(theme["section_header_text"]),
            fontName="Helvetica-Bold",
        )
        body_style = ParagraphStyle(
            "WABody",
            parent=styles["BodyText"],
            fontSize=10,
            leading=13,
            textColor=colors.HexColor(theme["body"]),
        )

        story: list = []
        story.append(Paragraph("WhatsApp Messaging Report", title_style))
        story.append(Paragraph(f"Scope: {scope_name}", body_style))
        story.append(Paragraph(f"Analysis Date: {analysis_date}", body_style))
        story.append(Spacer(1, 0.15 * inch))
        story.append(
            HRFlowable(
                width="100%", thickness=2, color=colors.HexColor(theme["accent_line"])
            )
        )
        story.append(Spacer(1, 0.15 * inch))

        chat = chat_metadata or {}
        if chat:
            story.append(Paragraph("Conversation", section_style))
            story.append(Spacer(1, 0.06 * inch))
            story.append(Paragraph(f"Title: {chat.get('title', 'N/A')}", body_style))
            if chat.get("preview"):
                story.append(Paragraph(f"Preview: {chat.get('preview')}", body_style))
            story.append(Spacer(1, 0.2 * inch))

        summary_data = [
            ["Metric", "Count"],
            ["Total Messages", str(stats.get("total", 0))],
            ["Positive", str(stats.get("positive", 0))],
            ["Negative", str(stats.get("negative", 0))],
            ["Neutral", str(stats.get("neutral", 0))],
            ["Questions", str(stats.get("questions", 0))],
            ["Suggestions", str(stats.get("suggestions", 0))],
            ["Spam", str(stats.get("spam", 0))],
            ["Unanswered", str(stats.get("unanswered", 0))],
            ["Replies Generated", str(stats.get("replies_generated", 0))],
            ["Replies Posted", str(stats.get("replies_posted", 0))],
            ["Replies Failed", str(stats.get("replies_failed", 0))],
        ]
        summary_table = Table(summary_data, colWidths=[3.5 * inch, 2.5 * inch])
        summary_table.setStyle(
            TableStyle(
                [
                    (
                        "BACKGROUND",
                        (0, 0),
                        (-1, 0),
                        colors.HexColor(theme["section_header_bg"]),
                    ),
                    ("TEXTCOLOR", (0, 0), (-1, 0), colors.whitesmoke),
                    ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                    ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
                    (
                        "ROWBACKGROUNDS",
                        (0, 1),
                        (-1, -1),
                        [colors.white, colors.HexColor("#f9f9f9")],
                    ),
                ]
            )
        )
        story.append(Paragraph("Comment Summary", section_style))
        story.append(Spacer(1, 0.08 * inch))
        story.append(summary_table)
        story.append(Spacer(1, 0.2 * inch))

        if top_opportunities:
            opp_data = [["Author", "Category", "Priority", "Comment"]]
            for opp in top_opportunities[:10]:
                opp_data.append(
                    [
                        opp.get("author", "")[:20],
                        opp.get("category", ""),
                        opp.get("engagement_priority", ""),
                        (opp.get("text", "")[:80] + "...")
                        if len(opp.get("text", "")) > 80
                        else opp.get("text", ""),
                    ]
                )
            opp_table = Table(
                opp_data, colWidths=[1.2 * inch, 1 * inch, 0.9 * inch, 3.5 * inch]
            )
            opp_table.setStyle(
                TableStyle(
                    [
                        (
                            "BACKGROUND",
                            (0, 0),
                            (-1, 0),
                            colors.HexColor(theme["section_header_bg"]),
                        ),
                        ("TEXTCOLOR", (0, 0), (-1, 0), colors.whitesmoke),
                        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                        ("FONTSIZE", (0, 0), (-1, -1), 8),
                        ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
                    ]
                )
            )
            story.append(Paragraph("Top Engagement Opportunities", section_style))
            story.append(Spacer(1, 0.08 * inch))
            story.append(opp_table)
            story.append(Spacer(1, 0.2 * inch))

        if generated_replies:
            reply_data = [["Author", "Category", "Reply"]]
            for reply in generated_replies[:15]:
                reply_data.append(
                    [
                        reply.get("author", "")[:20],
                        reply.get("category", ""),
                        (reply.get("reply_text", "")[:100] + "...")
                        if len(reply.get("reply_text", "")) > 100
                        else reply.get("reply_text", ""),
                    ]
                )
            reply_table = Table(
                reply_data, colWidths=[1.2 * inch, 1 * inch, 4.3 * inch]
            )
            reply_table.setStyle(
                TableStyle(
                    [
                        (
                            "BACKGROUND",
                            (0, 0),
                            (-1, 0),
                            colors.HexColor(theme["section_header_bg"]),
                        ),
                        ("TEXTCOLOR", (0, 0), (-1, 0), colors.whitesmoke),
                        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                        ("FONTSIZE", (0, 0), (-1, -1), 8),
                        ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
                    ]
                )
            )
            story.append(Paragraph("Generated Replies", section_style))
            story.append(Spacer(1, 0.08 * inch))
            story.append(reply_table)
            story.append(Spacer(1, 0.2 * inch))

        failed_replies = failed_replies or []
        if failed_replies:
            failed_data = [["Author", "Error", "Comment"]]
            for reply in failed_replies[:10]:
                failed_data.append(
                    [
                        reply.get("author", "")[:20],
                        (reply.get("post_error", "")[:60] + "...")
                        if len(reply.get("post_error", "")) > 60
                        else reply.get("post_error", ""),
                        (reply.get("text", "")[:80] + "...")
                        if len(reply.get("text", "")) > 80
                        else reply.get("text", ""),
                    ]
                )
            failed_table = Table(
                failed_data, colWidths=[1.2 * inch, 2.2 * inch, 3.1 * inch]
            )
            failed_table.setStyle(
                TableStyle(
                    [
                        (
                            "BACKGROUND",
                            (0, 0),
                            (-1, 0),
                            colors.HexColor(theme["section_header_bg"]),
                        ),
                        ("TEXTCOLOR", (0, 0), (-1, 0), colors.whitesmoke),
                        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                        ("FONTSIZE", (0, 0), (-1, -1), 8),
                        ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
                    ]
                )
            )
            story.append(Paragraph("Failed Reply Attempts", section_style))
            story.append(Spacer(1, 0.08 * inch))
            story.append(failed_table)
            story.append(Spacer(1, 0.2 * inch))

        if recommendations:
            story.append(Paragraph("Recommended Actions", section_style))
            story.append(Spacer(1, 0.08 * inch))
            for rec in recommendations:
                story.append(Paragraph(f"• {rec}", body_style))

        story.append(Spacer(1, 0.2 * inch))
        story.append(
            Paragraph(
                "Generated by WhatsApp Messaging Agent",
                ParagraphStyle(
                    "Footer",
                    parent=styles["Normal"],
                    fontSize=9,
                    textColor=colors.HexColor(theme["footer"]),
                    alignment=1,
                ),
            )
        )

        doc.build(story)
        file_size = os.path.getsize(output_path)
        return f"WhatsApp PDF report generated: {output_path} ({file_size} bytes)"
    except Exception as exc:
        return f"Error generating WhatsApp PDF report: {exc}"


